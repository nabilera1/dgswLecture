# print(1) 주석 다는 단축키 ctrl + /
print('1:', 10 > 5)  # True
print('2:', 10 < 3)  # False
print('3:', 10 > 5 or 10 < 3)  # True   ||, 또는
print('4:', 10 > 5 and 10 < 3)  # False &&, 그리고
print('5:', not True)  # False ! == not
print('6:', not True and True)  # False
print('7:', not True or True)  # True

